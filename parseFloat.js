let stringParaDecimal = "12.60";
console.log(stringParaDecimal); //Saída: "12.60"
console.log(typeof(stringParaDecimal)); //Saída: string
console.log(""); //Saída: ""
//Conversão da String "stringParaDecimal" para Decimal
let decimal = parseFloat(stringParaDecimal);
console.log(decimal); //Saída: 12.60
console.log(typeof(decimal)); //Saída: number